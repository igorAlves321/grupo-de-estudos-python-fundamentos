nomes = ["Macacoleta", "girofossauro", "giromiga", "jão sem perna"]

# Acessando elementos por índice
primeiro_nome = nomes[0]    # "Macacoleta"
segundo_nome = nomes[1]     # "girofossauro"
print(f"Primeiro nome: {primeiro_nome}")
print(f"Segundo nome: {segundo_nome}")

# Índices negativos acessam elementos de trás para frente
ultimo_nome = nomes[-1]     # "jão sem perna"
penultimo_nome = nomes[-2]  # "giromiga"
print(f"Último nome: {ultimo_nome}")
print(f"Penúltimo nome: {penultimo_nome}")